/***
 * function to calculate the random number
 * @param max
 * @returns {Number}
 */
function getRandomNumber(max){
	var limite_inferior=0;
	var limite_superior=max;
	var randomN=null;
	for(var i=0; i <= a.length-1; i++){
		if(max>0 && max<=10){
			  randomN=a[i]*10 % max;
			  limite_inferior=max-1;
			}else 
		if(max>10 && max<=100){
		  randomN=a[i]*10 % max;
		  limite_inferior=max-9;
		}else 
		if(max>100 && max<=1000){
		 randomN=a[i]*100 % max;
		 limite_inferior=max-90;
		}else
		if(max>1000 && max<=10000) {
		randomN=a[i]*10000 % max;
		limite_inferior=max-900;
		}else
		if(max>10000 && max<=100000) {
		randomN=a[i]*10000 % max;
		limite_inferior=max-9000;
		}else
		if(max>100000 && max<=500000) {
			randomN=a[i]*10000 % max;
			limite_inferior=max-90000;
		}else
		if(max>500000) {
			randomN=a[i]*500000 % max;
			limite_inferior=max-90000;
		}
		 
		if(randomN>limite_inferior && randomN<limite_superior){
			
	        return randomN;
		}
	}
	
	return randomN;


}

